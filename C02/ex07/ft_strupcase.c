char	*ft_strupcase(char *str)
{
	int		i;
	char	var;

	i = 0;
	while (str[i] != '\0')
	{
		var = str[i];
		if ((var > 96) && (var < 123))
		{
			str[i] = var - 32;
		}
		i++;
	}
	return (str);
}
